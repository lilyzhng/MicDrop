/**
 * Get source URL for a problem (if available).
 * These are typically interviewing.io links or other non-video source pages.
 */
export const getSourceUrl = (title: string): string | null => {
  if (PROBLEM_SOURCE_URLS[title]) {
    return PROBLEM_SOURCE_URLS[title];
  }
  
  // Try normalized match (case-insensitive)
  const normalizedTitle = title.toLowerCase().trim();
  for (const [key, url] of Object.entries(PROBLEM_SOURCE_URLS)) {
    if (key.toLowerCase().trim() === normalizedTitle) {
      return url;
    }
  }
  
  return null;
};

/**
 * Get NeetCode video URL for a problem title.
 * Uses mapping first, falls back to generated URL.
 */
export const getNeetCodeUrl = (title: string): string => {
  // Check direct match first
  if (NEETCODE_VIDEO_URLS[title]) {
    return NEETCODE_VIDEO_URLS[title];
  }
  
  // Try normalized match (case-insensitive)
  const normalizedTitle = title.toLowerCase().trim();
  for (const [key, url] of Object.entries(NEETCODE_VIDEO_URLS)) {
    if (key.toLowerCase().trim() === normalizedTitle) {
      return url;
    }
  }
  
  // Fallback to generated URL
  const slug = title
    .toLowerCase()
    .replace(/[^a-z0-9\s-]/g, '')
    .replace(/\s+/g, '-')
    .replace(/-+/g, '-')
    .trim();
  return `https://neetcode.io/problems/${slug}`;
};

